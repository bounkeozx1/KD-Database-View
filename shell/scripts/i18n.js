function _detectDefaultLang() {
  // 1) Honour an explicit saved choice.
  try { const saved = localStorage.getItem('kd_lang'); if (saved && I18N[saved]) return saved; } catch (e) {}
  // 2) Otherwise follow the device language (English device → English, Lao device → Lao, …).
  try {
    const nav = (navigator.language || navigator.userLanguage || '').toLowerCase();
    if (nav.startsWith('lo')) return 'lo';
    if (nav.startsWith('th')) return 'th';
    if (nav.startsWith('ko')) return 'ko';
    if (nav.startsWith('en')) return 'en';
  } catch (e) {}
  // 3) Fall back to a timezone hint.
  try {
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone || '';
    if (tz === 'Asia/Seoul' || tz === 'Asia/Pyongyang') return 'ko';
    if (tz === 'Asia/Vientiane') return 'lo';
    if (tz === 'Asia/Bangkok') return 'th';
  } catch (e) {}
  return 'en';
}
let currentLang = _detectDefaultLang();

function t(key, vars) {
  const dict = I18N[currentLang] || I18N.en;
  let str = dict[key] || I18N.en[key] || key;
  if (vars) Object.entries(vars).forEach(([k, v]) => { str = str.replace('{' + k + '}', v); });
  return str;
}

function setLang(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  localStorage.setItem('kd_lang', lang);
  applyTranslations();
}

function applyTranslations() {
  // Elements with data-i18n attribute get their textContent replaced
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  // Elements with data-i18n-placeholder get their placeholder replaced
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPh);
  });
  // Elements with data-i18n-title get their title (tooltip) replaced
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  // Update active lang button
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === currentLang);
  });
}
